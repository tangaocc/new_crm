package cn.tencent.crm.domain;

import java.util.Date;

/**
 * 系统日志
 */
public class SystemLog {
	private Long id;// ID
	private Employee opUser;// 操作用户
	private Date opTime;// 操作时间
	private String opIp;// 登录IP
	private String function;// 使用功能
	private String params;// 操作参数信息

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	
	public Employee getOpUser() {
		return opUser;
	}

	public void setOpUser(Employee opUser) {
		this.opUser = opUser;
	}

	public Date getOpTime() {
		return opTime;
	}

	public void setOpTime(Date opTime) {
		this.opTime = opTime;
	}

	public String getOpIp() {
		return opIp;
	}

	public void setOpIp(String opIp) {
		this.opIp = opIp;
	}

	public String getFunction() {
		return function;
	}

	public void setFunction(String function) {
		this.function = function;
	}

	public String getParams() {
		return params;
	}

	public void setParams(String params) {
		this.params = params;
	}

	@Override
	public String toString() {
		return "SystemLog [id=" + id + "   , opTime=" + opTime + ", opIp=" + opIp + ", function="
				+ function + ", params=" + params + "]";
	}

}
